#ifndef __UTF8_H__
#define __UTF8_H__

#define UC(S) \
	(((S[0] & 0xF8) == 0xF0) ? \
		(((S[0] & 0x07) << 18) | ((S[1] & 0x3F) << 12) | ((S[2] & 0x3F) << 6) | (S[3] & 0x3F)) : \
	((S[0] & 0xF0) == 0xE0) ? \
		(((S[0] & 0x0F) << 12) | ((S[1] & 0x3F) << 6) | (S[2] & 0x3F)) : \
	((S[0] & 0xE0) == 0xC0) ? \
		(((S[0] & 0x1F) << 6) | (S[1] & 0x3F)) : \
	S[0])

#endif /* __UTF8_H__ */
